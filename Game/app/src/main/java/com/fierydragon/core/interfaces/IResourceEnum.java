package com.fierydragon.core.interfaces;

/**
 * The IResourceEnum interface provides a method for enums to specify a resource path.
 * 
 * @author Chang Yi Zhong
 * @author Lim Hung Xuan
 */
public interface IResourceEnum {
    /**
     * Gets the resource path associated with the enum constant.
     * 
     * @return the resource path as a String
     */
    public String getResourcePath();
}
